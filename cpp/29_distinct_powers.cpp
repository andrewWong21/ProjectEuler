#include <iostream>

int distinct_powers(){
    int powers = 99 * 99;
    // x^2: 4, 9, 25, 36, 49, 100
    // 2^4 = 4^2 (2, 3, 4, ..., 50) 49
    powers -= 6*49;
    // x^3: 8 = 2^3 = 4^(3/2), 27 = 3^3 = 9^(3/2)
    // 2^6  = 8^2  ( 2,  3,  4, ..., 33)  32
    // 4^51 = 8^34 (34, 36, 38, ..., 66) +17
    powers -= 2*49;
    // x^4: 16 = 4^2 = 8^(4/3), 81 = 9^2 = 27^(4/3)
    // 4^4  = 16^2  ( 2,  3,  4, ..., 50)  49
    // 8^68 = 16^51 (51, 54, 57, ..., 75) + 9
    powers -= 2*58;
    // x^5: 32 = 8^(5/3) = 16^(5/4)
    //  2^10 = 32^2  ( 2,  3,  4, ..., 20)  19
    //  4^55 = 32^22 (22, 24, 26, ..., 40) +10
    //  8^35 = 32^21 (21, 24, 27, ..., 60) +11
    // 16^55 = 32^44 (44, 48, 52, ..., 80) + 8
    powers -= 48;
    // x^6: 64 = 8^2 = 16^(6/4) = 32^(6/5)
    //  8^4  = 64^2  ( 2,  3,  4, ..., 50)  49
    // 16^78 = 64^52 (52, 54, 56, ..., 66) + 8
    // 32^66 = 64^55 (55, 60, 65, ..., 80) + 5
    powers -= 62;
    return powers;
}

int main() {
    std::cout << distinct_powers() << "\n";
}
